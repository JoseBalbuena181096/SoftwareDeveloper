exports.sumar = function (numero1, numero2) {
  return numero1 + numero2;
};

exports.restar = function (numero1, numero2) {
  return numero1 - numero2;
};

exports.multiplicar = function (numero1, numero2) {
  return numero1 * numero2;
};

exports.dividir = function (numero1, numero2) {
  return numero1 / numero2;
};
